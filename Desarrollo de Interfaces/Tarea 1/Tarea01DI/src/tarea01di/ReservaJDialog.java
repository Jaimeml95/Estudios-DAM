    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed

        // 1. Extraer la fecha y darle formato de texto para el mensaje
        // Aunque el Spinner solo muestre dd/MM/yyyy, getValue() devuelve un objeto Date complejo
        java.text.SimpleDateFormat formato = new java.text.SimpleDateFormat("dd/MM/yyyy");
        String fechaSeleccionada = formato.format(spnFecha.getValue());

        // 2. Extraer la hora seleccionada del ComboBox
        String horaSeleccionada = cbHora.getSelectedItem().toString();

        // 3. Extraer el nombre para personalizar el mensaje
        String cliente = txtNombre.getText();

        // 4. Construir el cuerpo del mensaje
        String mensaje = "Reserva confirmada con éxito.\n\n"
                + "Cliente: " + cliente + "\n"
                + "Fecha: " + fechaSeleccionada + "\n"
                + "Hora: " + horaSeleccionada;

        // 5. Mostrar el mensaje al usuario
        javax.swing.JOptionPane.showMessageDialog(this, mensaje, "Confirmación de Reserva", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        // 6. Cerrar el diálogo
        this.dispose();
    }//GEN-LAST:event_btnEnviarActionPerformed

    /**
     * @param args the command line arguments
     */
    private void gestionarCamposAmistoso() {
        // Esta línea es la clave: devuelve 'true' solo si Amistoso está marcado
        boolean esAmistoso = rbAmistoso.isSelected();

        // Si esAmistoso es false (porque está pulsado Liga o Clase), se deshabilitan:
        spnNivel.setEnabled(esAmistoso);
        chkPartidoAbierto.setEnabled(esAmistoso);

        // Lógica para el Slider de plazas
        if (esAmistoso && chkPartidoAbierto.isSelected()) {
            sldPlazasLibres.setEnabled(true);
        } else {
            sldPlazasLibres.setEnabled(false);
        }
    }

