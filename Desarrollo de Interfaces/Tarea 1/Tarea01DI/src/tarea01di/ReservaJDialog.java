    private void cbPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbPaisActionPerformed

        // Obtenemos el texto de la opción seleccionada
        String seleccion = cbPais.getSelectedItem().toString();

        // Estructura para asignar el prefijo según el país
        // Estructura para asignar el prefijo según el país
        switch (seleccion) {
            case "ES":
                txtPrefijo.setText("+34");
                break;
            case "FR":
                txtPrefijo.setText("+33");
                break;
            case "PT":
                txtPrefijo.setText("+351");
                break;
            default:
                txtPrefijo.setText("");
                break;
        }
    }//GEN-LAST:event_cbPaisActionPerformed

