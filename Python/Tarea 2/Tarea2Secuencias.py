nombres = ["Beatriz", "Marcos", "Lucia", "Adrian", "Elena"]

# 1. Primero y último
print(f"Primero: {nombres[0]}, Último: {nombres[-1]}")

# 2. Ordenar alfabéticamente
nombres.sort()
print(f"Lista ordenada: {nombres}")

# 3. Agregar nombre nuevo
nombres.append("Roberto")
print(f"Lista tras añadir a Roberto: {nombres}")